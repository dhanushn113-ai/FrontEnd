// Assignment Operators in javascript
// Assignment operators are used to assign values to variables. The most common assignment operator is the equal sign (=),
//  which assigns the value on the right side of the operator to the variable on the left side. 
// There are also compound assignment operators that combine an arithmetic operation with assignment, such as +=, -=, *=, and /=.

let a = 10;
let b = 5;

a += b;
console.log(a); // it will print 15 because a = a + b => 10 + 5 = 15

a -= b;
console.log(a); // it will print 10 because a = a - b => 15 - 5 = 10

a *= b;
console.log(a); // it will print 50 because a = a * b => 10 * 5 = 50

a /= b;
console.log(a); // it will print 10 because a = a / b => 50 / 5 = 10



