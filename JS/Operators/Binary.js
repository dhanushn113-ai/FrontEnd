// understand the binary operators in JavaScript
// Binary operators are operators that operate on two operands. In JavaScript, the binary operators include:
// 1. Addition (+): This operator adds two operands together. If either operand is a string, it performs string concatenation instead of numeric addition.
// 2. Subtraction (-): This operator subtracts the second operand from the first operand.
// 3. Multiplication (*): This operator multiplies two operands together.
// 4. Division (/): This operator divides the first operand by the second operand.-quotient
// 5. Modulus (%): This operator returns the remainder of the division of the first operand by the second operand.
// 6. Exponentiation (**): This operator raises the first operand to the power of the second operand.

let a = 10;
let b = 20;

console.log(a+b);     
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);    