// understand unary operators in JavaScript
// Unary operators are operators that operate on a single operand. In JavaScript, the unary operators include:

// 1. Unary plus (+): This operator converts its operand to a number. If the operand is already a number, it has no effect. 
// 2. Unary negation (-): This operator negates its operand, converting positive numbers to negative and vice versa.

let a = 1;
a = -a;                    // it will convert a into negative number
console.log(a);

let b = -1;
b = -b;                       // it will convert b into positive number
console.log(b);

