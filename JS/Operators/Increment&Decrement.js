// understanding increment and decrement operators in JavaScript
// Increment and decrement operators are used to increase or decrease the value of a variable by 1.
// The increment operator (++) adds 1 to the variable, while the decrement operator (--) subtracts 1 from the variable.

let x = 5;
let y = 10;

x++; // x = x + 1
console.log(x); // it will print 6

y--; // y = y - 1
console.log(y); // it will print 9
let z = 15;
console.log(z--); // it will print 15 because the decrement operator is used after the variable, so the value of z is printed first and then decremented
console.log(z); // it will print 14